package Game;

public class Bag {
    private int potions;
    private int pokeballs;
    public void setPotions(int potions){
        this.potions = potions;
    }
    public void setPokeballs(int pokeballs){
        this.pokeballs = pokeballs;
    }
    public int getPotions(){
        return potions;
    }
    public int getPokeballs(){
        return pokeballs;
    }
}
